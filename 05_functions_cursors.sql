-- ------------------------------------------------------------
-- FUNCTION 1: Calculate_Total_Price
-- Returns total price for a given product and quantity
-- Also validates product exists and is active
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION Calculate_Total_Price (
    p_product_id IN Products.ProductID%TYPE,
    p_quantity   IN NUMBER
)
RETURN NUMBER
AS
    v_unit_price  Products.Unit_Price%TYPE;
    v_prod_name   Products.Name%TYPE;
BEGIN
    -- Validate product and fetch price
    SELECT Unit_Price, Name
    INTO   v_unit_price, v_prod_name
    FROM   Products
    WHERE  ProductID = p_product_id
    AND    Is_Active = 1;

    RETURN ROUND(v_unit_price * p_quantity, 2);

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20010,
            'ERROR: Product ID ' || p_product_id ||
            ' not found or inactive.');
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20011,
            'ERROR in Calculate_Total_Price: ' || SQLERRM);
END Calculate_Total_Price;
/


-- ------------------------------------------------------------
-- FUNCTION 2: Get_Stock_Status
-- Returns stock status label for a product
-- Returns: 'LOW STOCK', 'OK', 'OUT OF STOCK', or 'NOT FOUND'
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION Get_Stock_Status (
    p_product_id IN Products.ProductID%TYPE
)
RETURN VARCHAR2
AS
    v_quantity      Stock_Management.Quantity%TYPE;
    v_restock_level Stock_Management.Restock_Level%TYPE;
BEGIN
    SELECT Quantity, Restock_Level
    INTO   v_quantity, v_restock_level
    FROM   Stock_Management
    WHERE  ProductID = p_product_id;

    IF v_quantity = 0 THEN
        RETURN 'OUT OF STOCK';
    ELSIF v_quantity < v_restock_level THEN
        RETURN 'LOW STOCK';
    ELSE
        RETURN 'OK';
    END IF;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 'NOT FOUND';
END Get_Stock_Status;
/


-- ------------------------------------------------------------
-- FUNCTION 3: Is_Category_Active
-- Returns 1 if category is active, 0 if inactive
-- Used internally by procedures and packages
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION Is_Category_Active (
    p_category_id IN Category.CategoryID%TYPE
)
RETURN NUMBER
AS
    v_is_active Category.IS_ACTIVE%TYPE;
BEGIN
    SELECT IS_ACTIVE
    INTO   v_is_active
    FROM   Category
    WHERE  CategoryID = p_category_id;

    RETURN v_is_active;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN -1;  -- Category does not exist
END Is_Category_Active;
/


-- ------------------------------------------------------------
-- FUNCTION 4: Get_Total_Sales_By_Product
-- Returns total revenue generated by a product across all sales
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION Get_Total_Sales_By_Product (
    p_product_id IN Products.ProductID%TYPE
)
RETURN NUMBER
AS
    v_total NUMBER(12,2) := 0;
BEGIN
    SELECT NVL(SUM(ti.Subtotal), 0)
    INTO   v_total
    FROM   Transaction_Items ti
    JOIN   Transactions      t  ON ti.TransactionID = t.TransactionID
    WHERE  ti.ProductID  = p_product_id
    AND    t.Trans_Type  = 'SALE';

    RETURN v_total;

EXCEPTION
    WHEN OTHERS THEN
        RETURN 0;
END Get_Total_Sales_By_Product;
/


